    public void tampilkan_data(){
       model= new DefaultTableModel();
       jTable_kamera.setModel(model);
       model.addColumn("kode_kamera");
       model.addColumn("nama_kamera");
       model.addColumn("jenis_kamera");
       model.addColumn("harga_kamera");
       
       try{
       Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sewa_kamera","root","");
      ResultSet res=cn.createStatement().executeQuery("select * FROM kamera");
      while(res.next()){
          Object[]obj= new Object[4];
          obj[0]=res.getString("kd_kamera");
          obj[1]=res.getString("nama_kamera");
          obj[2]=res.getString("jenis_kamera");
          obj[3]=res.getString("harga_kamera");
          
          model.addRow(obj);
      }
   }
       catch(SQLException err){
           JOptionPane.showMessageDialog(null, err.getMessage());
       }
    }
