    private void autonumber(){
    String kd_kamera= "KM0000";
    int i = 0;
    try{
    Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sewa_kamera","root","");
    Statement st =cn.createStatement();
    String sql= "select kd_kamera from kamera";
    ResultSet rs = st.executeQuery(sql);
    while(rs.next()){
	kd_kamera = rs.getString("kd_kamera");
    }
    kd_kamera  = kd_kamera.substring(4);
    i = Integer.parseInt(kd_kamera)+1;
    kd_kamera = "00"+i;
    kd_kamera = "KM"+ kd_kamera.substring(kd_kamera.length()-3);
    jTkode_kamera.setText(kd_kamera);
    } catch (SQLException e) {
	System.out.println(e.getMessage());
    }
}

